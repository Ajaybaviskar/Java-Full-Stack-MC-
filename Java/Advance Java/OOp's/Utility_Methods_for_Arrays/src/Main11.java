// How to Manipulate Arrays in Java 

import java.util.Arrays;

public class Main11 {
	public static void main(String args[]) {
		int[] array= {2,3,1}; // Here we create Array and insert three values 1,2,3 
		Arrays.sort(array);  // Here we sort array Using Array.sort() Method
		System.out.println(Arrays.toString(array)); // and Here we convert Array to String. 
	}

}
